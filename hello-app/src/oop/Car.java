package oop;

public class Car {
    private int name;
    private int number;
    private int a;

    public int getName() {
        return name;
    }

    public void setName(int name) {
        this.name = name;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }
}
